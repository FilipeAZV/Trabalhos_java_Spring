//Esse comando avalia uma expressão e caso verdadeira executa o bloco dentro do comando while.

public class WhileDoWhilhe {

	public static void main(String[] args) {

		int i = 1;
		int max = 10;

		while (i < max) {
			System.out.println("Valor de i: " + i);
			i++; // i = i + 1
		}
	}
}
